#include <string.h>

#include "compiler.h"
#include "macros.h"
#include "object.h"
#include "prims.h"
#include "type.h"
#include "vm.h"

int main(int argc, char *argv[])
{
  char *inputs[] = {
      "(expand-macro '(pset! n m m (mod n m)))",
      "(expand-macro '(name-lambda gcd (n m) (if (= m 0) n (gcd m (mod n m)))))",
      "gcd",
      "(gcd 2 4)", // 2
      "(gcd 4 2)", // 2
      "(gcd 81 90)", // 9
      "(gcd 90 81)", // 9
  };
  init_global_variable();
  init_prims();
  init_compiled_prims();
  init_macros();
  load_init_file();
  for (int i = 0; i < sizeof(inputs) / sizeof(char *); i++) {
    writef(standard_out, ">> %s\n", make_string(inputs[i]));
    lisp_object_t *expr = read_object_from_string(strdup(inputs[i]));
    expr = compile_to_bytecode(expr);
    if (!is_signaled(expr))
      expr = run_by_llam(expr);
    if (is_signaled(expr))
      writef(standard_out, "%?\n", expr);
    else
      writef(standard_out, "=> %?\n", expr);
  }
  return 0;
}
